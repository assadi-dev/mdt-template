    public function findAccessById($id)
    {
        $qb = $this->createQueryBuilder("g");
        $qb->select("a.page,a.path,a.isCanAdd,a.isCanUpdate,a.isCanDelete")
        ->innerJoin(Access::class, "a", "WITH", "a.grade=g.id")
        ->where("g.id=:id")
        ->setParameter("id", $id)
        ;
        $result = $qb->getQuery()->getArrayResult();

        return $result;

